package Lesson4Homework;

public class NumberCheck {
    public static void main(String[] args) {
        int value = 7;
        if (value >= 0) {
            System.out.println(value + " este numar pozitiv");

        }
        if (value < 0) {
            System.out.println(value + " este numar negativ");
        }
        if (value != 0) {
            System.out.println(value + " nu este 0");
        }

    }
}